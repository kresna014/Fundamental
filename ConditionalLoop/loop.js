// looping -> for
 
for (let i = 1; i <= 10 ; i++) { // (;) wajib pakai // i = 0 loop dari 0-10 // i = 1 loop dari 1-9 // (=) sampai batasan yg d tentukan (<>=)
    console.log(`Hello Bandung ke-${i}`)
}

for (let i = 11; i > 0; i-=2) {
    console.log(`Hello Bandung ke-${i}`)
}

for (let i = 1; i <= 10; i++) {
    console.log(i)
}

// = hitungan sampe 0 jika tidak sampe 1


